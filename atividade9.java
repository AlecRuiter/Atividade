public class atividade9 {
    public static void main(String[] args) {
        
        int  maiorNumero= 50;

        int contador = 1;
        while (contador <= maiorNumero) {
            System.out.println(contador);
            contador += 2;
        }

        System.out.println("Números ímpares até " + maiorNumero + ":");
    }
}
