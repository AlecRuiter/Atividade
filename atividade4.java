import java.util.Scanner;

public class atividade4 {

    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        int population1 = 80000;
        int population2 = 200000;

        double taxaCrescimentoA = 0.03; 
        double taxaCrescimentoB = 0.015; 

        int years = 0;


        while (population2 < population2 ) {

            population1 += population1 * taxaCrescimentoA;
            population2 += population2 * taxaCrescimentoB;
            years++; 
        }


        System.out.println("Serão necessários " + years + " anos para que a população do país A ultrapasse ou iguale a população do país B.");
    }
}


        

